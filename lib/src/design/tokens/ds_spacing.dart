/// Spacing scale used across the design system.
///
/// Adopts the genfitai scale verbatim — covers every margin/padding
/// value seen in klozy pages (4, 8, 12, 16, 24, 32, 48) plus
/// genfitai-scale steps not yet used in klozy (20, 40, 56).
abstract final class DSSpacing {
  static const double xxxs = 4;
  static const double xxs = 8;
  static const double xs = 12;
  static const double s = 16;
  static const double m = 20;
  static const double l = 24;
  static const double xl = 32;
  static const double xxl = 40;
  static const double xxxl = 48;
  static const double xxxxl = 56;
}
